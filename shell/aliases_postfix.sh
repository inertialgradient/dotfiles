# search for the given term
alias -g G='| grep --line-number --context=1'

# copy to clipboard
alias -g C="| pbcopy"

# see head of piped-in output
alias -g H='| head'

# see the tail of piped-in output
alias -g T='| tail'

# watch tail of piped-in output
alias -g W='| tail -f'

# send to less
alias -g L='| less'
